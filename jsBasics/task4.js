// 4.1 Area of circle
let circleRadius = 5.2;
let circleArea = Math.PI * Math.pow(circleRadius, 2);
console.log("Area of circle:", circleArea.toFixed(2));

// 4.2 Area of rectangle
let length = 11;
let width = 3;
let rectangleArea = length * width;
console.log("Area of rectangle:", rectangleArea.toFixed(2));

// 4.3 Cylinder capacity
const cylinderRadius = 2.5;
const cylinderHeight = 10;
const cylinderVolume = Math.PI * Math.pow(cylinderRadius, 2) * cylinderHeight;
console.log("Cylinder capacity:", cylinderVolume.toFixed(2));